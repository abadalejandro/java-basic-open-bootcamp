package com.company;

public class Variables {
    public static void main(String[] args) {
        // enteros
        byte number1 = 1; // 1 byte
        short number2 = 2; // 2 byte
        int number3 = 3; // 4 byte
        long number4 = 4l; // 8 byte

        // punto flotante
        float decimal1 = 4.9f;
        double decimal2 = 9.99d;


        // caracter
        char caracter1 = 'a';

        // boolean
        boolean verdadero = true;


        // cadenas de texto
        String nombre = "Ale";


        // tipos envoltorios
        Integer numero = null;
        Long numero2 = 2L;


    }
}